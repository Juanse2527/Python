### Operadores aritmeticos ###

print(3 + 4)  # Suna
print(3 - 4)  # Resta
print(3 * 4)  # Multipliocacion
print(3 / 4)  # Division
print(3 % 4)  # Modulo
print(3 // 4)  # Aproxima la divison a entera 
print(3 ** 4)  # Calcular un exponente 
print("Hola" + "Python")  # Concatenacion 
print("hola" + str(5))  # Concatenacion y conversion de int a str
print(" hola " *  4)  # Multiplica el string
print("------------------------------------------------------------------------")
my_float = 2.5 * 2
print(" hola " * int(my_float))

print("------------------------------------------------------------------------")

### Operadores comparativos ###

print (3 > 4)
print (3 < 4)
print (3 >= 4)
print (3 <= 4)
print (3 == 4)
print (3 != 4)

print("------------------------------------------------------------------------")


### Operadores Logicos ###
print (3 > 4 and "hola" > "python")
print (3 < 4 or "hola" > "python")
print (not(3 >= 4 ))


